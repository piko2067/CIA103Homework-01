package hw1;

public class hw2 {
	
	public static void main(String[] args) {
		// 1.通過指定運算子 = ，設定雞蛋總數。
		int x=200;
		// 2.通過指定運算子 = ，設定一打雞蛋數量(一打為12顆)。
		int y=12;
		// 3.通過算數運算子 / ，算出共是幾打雞蛋。
		int z=x/y;
		System.out.println("雞蛋打數="+z);
		// 4.通過算數運算子 * ，算出共是幾顆雞蛋。
		System.out.println("雞蛋蛋數="+y*z);
	}
}
